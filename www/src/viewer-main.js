/**
 * Viewer bundle entry point.
 *
 * This file is the Vite entry for the shared viewer bundle.
 * It imports:
 *   1. All feezal element packages (via feezal-elements.js generated at server startup)
 *   2. The viewer app components
 *   3. The connection module (dynamically loads mqtt backend when connected)
 *
 * The resulting viewer-bundle.js is embedded in server-generated viewer pages.
 * No feezal backend required at viewer runtime — only MQTT-WS to a broker.
 */

// feezal-connection MUST be defined before any element package is imported.
// When customElements.define() runs for a Polymer element, the browser
// immediately upgrades any already-connected instance and calls its
// connectedCallback, which calls feezal.connection.subscribe. If
// feezal-connection isn't defined yet, feezal.connection is a plain
// HTMLElement without subscribe → TypeError.
import './feezal-connection.js';

// Viewer app components
import './feezal-app-viewer.js';
import './feezal-site.js';
import './feezal-view.js';

// Element packages (all installed feezal elements + themes)
// This file is generated by the server's element scanner at startup.
// Run `node server/bin/feezal.js` once before building to populate it.
import '../editor/feezal-elements.js';
